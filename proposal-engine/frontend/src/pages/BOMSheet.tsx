// ─────────────────────────────────────────────
// Grouped BOM table (single <table>, category group header rows)
// ─────────────────────────────────────────────

function BomTable({
  fields, control, register, remove, append, liveRows, initialRows, allCollapsed, canEdit,
}: {
  fields:       { id: string }[];
  control:      ReturnType<typeof useForm<FormValues>>['control'];
  register:     ReturnType<typeof useForm<FormValues>>['register'];
  remove:       (index: number) => void;
  append:       (row: BomRow) => void;
  liveRows:     BomRow[];
  initialRows:  BomRow[];
  allCollapsed: boolean;
  canEdit:      boolean;
}) {
  const [collapsed, setCollapsed] = useState<Set<string>>(new Set());
  const toggle = (cat: string) =>
    setCollapsed((prev) => { const n = new Set(prev); n.has(cat) ? n.delete(cat) : n.add(cat); return n; });

  // Sync to parent's expand/collapse-all signal
  const prevAllCollapsed = React.useRef(allCollapsed);
  useEffect(() => {
    if (prevAllCollapsed.current === allCollapsed) return;
    prevAllCollapsed.current = allCollapsed;
    if (allCollapsed) {
      setCollapsed(new Set(CATEGORIES.map((c) => c.value)));
    } else {
      setCollapsed(new Set());
    }
  }, [allCollapsed]);

  // Use liveRows for category when available, fall back to initialRows (always correct on first render)
  const getCat = (i: number): string =>
    liveRows[i]?.category || initialRows[i]?.category || 'others';

  const grouped = CATEGORIES.map((cat) => ({
    cat,
    indices: fields.map((_, i) => i).filter((i) => getCat(i) === cat.value),
  })).filter((g) => g.indices.length > 0);

  if (fields.length === 0 || (fields.length === 1 && !liveRows[0]?.itemName)) {
    return (
      <div className="bg-white rounded-xl shadow-sm border border-primary-100 px-4 py-10 text-center text-secondary-400 text-sm">
        No BOM items yet. Save a Costing Sheet first to auto-generate the BOM.
      </div>
    );
  }

  return (
    <div className="bg-white rounded-xl shadow-sm border border-primary-100 overflow-x-auto">
      <table className="w-full border-collapse min-w-[920px]">
        <thead>
          <tr className="border-b-2 border-primary-200 bg-primary-50/80">
            <th className="px-3 py-3 text-left text-xs font-semibold text-secondary-500 uppercase tracking-wide w-36">Category</th>
            <th className="px-3 py-3 text-left text-xs font-semibold text-secondary-500 uppercase tracking-wide w-16">Source</th>
            <th className="px-3 py-3 text-left text-xs font-semibold text-secondary-500 uppercase tracking-wide">Item / Description</th>
            <th className="px-3 py-3 text-left text-xs font-semibold text-secondary-500 uppercase tracking-wide">Specification</th>
            <th className="px-3 py-3 text-right text-xs font-semibold text-secondary-500 uppercase tracking-wide w-20">Qty</th>
            <th className="px-3 py-3 text-left text-xs font-semibold text-secondary-500 uppercase tracking-wide w-36">Brand</th>
            <th className="px-3 py-3 text-center text-xs font-semibold text-blue-600 uppercase tracking-wide w-20">GST %</th>
            <th className="w-8" />
          </tr>
        </thead>
        <tbody>
          {grouped.map(({ cat, indices }) => {
            const isOpen   = !collapsed.has(cat.value);
            const accent   = catAccentColor(cat.value);
            const catRows  = indices.map((i) => liveRows[i]).filter(Boolean);
            const totalQty = catRows.reduce((s, r) => s + (parseFloat(r.quantity) || 0), 0);

            return (
              <React.Fragment key={cat.value}>
                {/* ── Category group header row ── */}
                <tr
                  className="cursor-pointer select-none hover:brightness-95 transition-all"
                  style={{ background: `${accent}18`, borderTop: `2px solid ${accent}40` }}
                  onClick={() => toggle(cat.value)}
                >
                  <td colSpan={9} className="px-3 py-2">
                    <div className="flex items-center justify-between">
                      <div className="flex items-center gap-2.5">
                        <span
                          className="text-xs font-bold transition-transform duration-200 inline-block"
                          style={{ color: accent, transform: isOpen ? 'rotate(90deg)' : 'rotate(0deg)' }}
                        >▶</span>
                        <span className={`text-xs px-2.5 py-0.5 rounded-full border font-bold ${CATEGORY_COLORS[cat.value as keyof typeof CATEGORY_COLORS] ?? ''}`}>
                          {cat.label}
                        </span>
                        <span className="text-xs text-secondary-500">
                          {indices.length} item{indices.length !== 1 ? 's' : ''}
                        </span>
                      </div>
                      <div className="flex items-center gap-3">
                        {totalQty > 0 && (
                          <span className="text-xs font-bold tabular-nums" style={{ color: accent }}>
                            Qty: {totalQty.toLocaleString('en-IN')}
                          </span>
                        )}
                        {canEdit && (
                        <button
                          type="button"
                          onClick={(e) => {
                            e.stopPropagation();
                            append({ ...EMPTY_ROW, category: cat.value });
                            setCollapsed((prev) => { const n = new Set(prev); n.delete(cat.value); return n; });
                          }}
                          className="text-xs font-semibold px-2.5 py-0.5 rounded-lg border transition-colors hover:opacity-80"
                          style={{ color: accent, borderColor: `${accent}60`, background: 'white' }}
                        >
                          + Add row
                        </button>
                        )}
                      </div>
                    </div>
                  </td>
                </tr>

                {/* ── Data rows ── */}
                {isOpen && indices.map((globalIdx) => (
                  <BomRowInput
                    key={fields[globalIdx]?.id ?? globalIdx}
                    index={globalIdx}
                    control={control}
                    register={register}
                    onRemove={() => remove(globalIdx)}
                    category={cat.value}
                    canEdit={canEdit}
                  />
                ))}

                {/* ── Category qty subtotal ── */}
                {isOpen && totalQty > 0 && (
                  <tr style={{ background: `${accent}08`, borderBottom: `1px solid ${accent}30` }}>
                    <td colSpan={4} className="px-3 py-1.5 text-xs text-secondary-400 italic pl-10">
                      {cat.label} subtotal
                    </td>
                    <td className="px-3 py-1.5 text-right text-sm font-bold tabular-nums" style={{ color: accent }}>
                      {totalQty.toLocaleString('en-IN')}
                    </td>
                    <td colSpan={4} />
                  </tr>
                )}
              </React.Fragment>
            );
          })}
        </tbody>
      </table>
    </div>
  );
}

