function exportBomXlsx(rows: BomRow[], sheetName: string) {
  const headerRow = ['#', 'Item / Description', 'Specification', 'Qty', 'Brand', 'GST %'];
  const dataRows  = rows
    .filter((r) => r.itemName.trim())
    .map((r, i) => [
      i + 1,
      r.itemName,
      r.specification,
      parseFloat(r.quantity) || 0,
      r.brand || '',
      deriveGstRate(r.itemName),
    ]);

  const totalQty = rows.reduce((s, r) => s + (parseFloat(r.quantity) || 0), 0);
  const blankRow = ['', '', '', '', '', ''];
  const totalRow = ['', 'TOTAL LINE ITEMS', '', totalQty, '', ''];
  const countRow = ['', 'TOTAL ITEMS IN BOM', '', `${rows.filter((r) => r.itemName.trim()).length} line items`, '', ''];

  const allRows = [headerRow, ...dataRows, blankRow, totalRow, countRow];

  const ws = XLSX.utils.aoa_to_sheet(allRows);
  ws['!cols'] = [{ wch: 4 }, { wch: 36 }, { wch: 24 }, { wch: 8 }, { wch: 20 }, { wch: 8 }];

  const wb = XLSX.utils.book_new();
  XLSX.utils.book_append_sheet(wb, ws, 'Bill of Materials');
  XLSX.writeFile(wb, `${sheetName.replace(/[^a-zA-Z0-9_\-. ]/g, '_')}_BOM.xlsx`);
}

function exportBomCsv(rows: BomRow[], sheetName: string) {
  const header = '#,Item / Description,Specification,Qty,Brand,GST %';

  const escape = (v: string | number) => {
    const s = String(v);
    return s.includes(',') || s.includes('"') ? `"${s.replace(/"/g, '""')}"` : s;
  };

  const dataRows = rows
    .filter((r) => r.itemName.trim())
    .map((r, i) =>
      [i + 1, r.itemName, r.specification, parseFloat(r.quantity) || 0, r.brand || '', deriveGstRate(r.itemName)].map(escape).join(','),
    );

  const totalQty  = rows.reduce((s, r) => s + (parseFloat(r.quantity) || 0), 0);
  const summaryLines = [
    `,`,
    `,"TOTAL LINE ITEMS",,${totalQty}`,
    `,"TOTAL ITEMS IN BOM",,${rows.filter((r) => r.itemName.trim()).length} line items`,
  ];

  const csv = [header, ...dataRows, ...summaryLines].join('\n');
  const blob = new Blob(['\uFEFF' + csv], { type: 'text/csv;charset=utf-8;' });
  const url  = URL.createObjectURL(blob);
  const a    = document.createElement('a');
  a.href     = url;
  a.download = `${sheetName.replace(/[^a-zA-Z0-9_\-. ]/g, '_')}_BOM.csv`;
  a.click();
  URL.revokeObjectURL(url);
}

