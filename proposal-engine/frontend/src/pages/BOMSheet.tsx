export default function BOMSheet() {
  // Resolve initial rows synchronously to avoid useEffect lag
  const _initRows = (() => {
    const ac = getActiveCustomer();
    if (ac?.bom?.rows && ac.bom.rows.length > 0)
      return (ac.bom.rows as Partial<BomRow>[]).map(normalizeRow);
    const stored = loadStoredBom();
    if (stored) {
      const overrides = loadOverrides();
      if (overrides && overrides.sheetId === stored.sheetId)
        return overrides.rows.map(normalizeRow);
      return (stored.rows as Partial<BomRow>[]).map(normalizeRow);
    }
    return [];
  })();

  // Keep a ref so BomTable always has correct categories on first render
  const initialRowsRef = useRef<BomRow[]>(_initRows);

  const [loading, setLoading]       = useState(_initRows.length === 0);
  const [source, setSource]         = useState<'costing-sheet' | 'overrides' | 'api' | null>(
    _initRows.length > 0 ? 'overrides' : null
  );
  const [storedBom, setStoredBom]   = useState<StoredBom | null>(loadStoredBom);
  const [fetchError, setFetchError] = useState<string | null>(null);
  const [saveStatus, setSaveStatus] = useState<'idle' | 'saving' | 'saved' | 'error'>('idle');
  const [allCollapsed, setAllCollapsed] = useState(false);

  const canEdit = canEditProposalArtifacts();

  const { control, register, handleSubmit, reset } = useForm<FormValues>({
    defaultValues: { rows: _initRows },
  });

  const { fields, append, remove } = useFieldArray({ control, name: 'rows' });
  const watchedRows = useWatch({ control, name: 'rows' }) as BomRow[] | undefined;
  const liveRows = watchedRows ?? [];

  const loadBom = useCallback((force = false) => {
    // If we already loaded synchronously on init, skip unless forced
    if (!force && _initRows.length > 0) {
      setLoading(false);
      return;
    }

    setLoading(true);
    setFetchError(null);

    // 1. Prefer active customer's saved BOM artifact
    const ac = getActiveCustomer();
    if (ac?.bom?.rows && ac.bom.rows.length > 0) {
      const rows = (ac.bom.rows as Partial<BomRow>[]).map(normalizeRow);
      initialRowsRef.current = rows;
      reset({ rows });
      setSource('overrides');
      setLoading(false);
      return;
    }

    // 2. Fall back to localStorage costing-sheet-generated BOM
    const stored = loadStoredBom();
    setStoredBom(stored);

    if (stored) {
      const overrides = loadOverrides();
      if (overrides && overrides.sheetId === stored.sheetId) {
        const rows = overrides.rows.map(normalizeRow);
        initialRowsRef.current = rows;
        reset({ rows });
        setSource('overrides');
      } else {
        const rows = (stored.rows as Partial<BomRow>[]).map(normalizeRow);
        initialRowsRef.current = rows;
        reset({ rows });
        setSource('costing-sheet');
      }
      setLoading(false);
      return;
    }

    // No localStorage data and no backend in production — start with empty BOM
    setSource('costing-sheet');
    setLoading(false);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [reset]);

  useEffect(() => { loadBom(); }, [loadBom]);

  const onSubmit = useCallback((data: FormValues) => {
    setSaveStatus('saving');
    try {
      const now     = new Date().toISOString();
      const sheetId = storedBom?.sheetId ?? 'manual';
      persistOverrides(sheetId, data.rows);
      setSource('overrides');

      // Forward ROI autofill from the costing sheet (BOM doesn't have costs,
      // so we preserve whatever the costing sheet wrote — just update the source label)
      try {
        const wip = getWipKeysForCurrentUser();
        const existing = localStorage.getItem(wip.roiAutofill);
        if (existing) {
          const prev: RoiAutofill = JSON.parse(existing);
          const updated: RoiAutofill = {
            ...prev,
            source:     'bom',
            sourceName: storedBom?.sheetName ?? prev.sourceName,
            savedAt:    now,
          };
          localStorage.setItem(wip.roiAutofill, JSON.stringify(updated));
        }
      } catch { /* ignore */ }

      // Persist BOM artifact to active customer record
      const activeCustomer = getActiveCustomer();
      if (activeCustomer) {
        const artifact: BomArtifact = { savedAt: now, rows: data.rows };
        upsertCustomer({ ...activeCustomer, bom: artifact, updatedAt: now });

        // Best-effort sync to CRM backend for CRM-linked projects.
        if (activeCustomer.master.crmProjectId) {
          void syncProjectBom(activeCustomer.master.crmProjectId, artifact);
        }
      }

      setSaveStatus('saved');
      setTimeout(() => setSaveStatus('idle'), 2500);
    } catch {
      setSaveStatus('error');
      setTimeout(() => setSaveStatus('idle'), 3000);
    }
  }, [storedBom]);

  const handleRegenerateFromCosting = useCallback(() => {
    const stored = loadStoredBom();
    if (!stored) return;
    // Clear overrides and reload the raw generated rows
    localStorage.removeItem(getWipKeysForCurrentUser().bomOverrides);
    const rows = (stored.rows as Partial<BomRow>[]).map(normalizeRow);
    initialRowsRef.current = rows;
    reset({ rows });
    setSource('costing-sheet');
  }, [reset]);

  return (
    <div>
      {/* Page card */}
      <div className="bg-gradient-to-br from-white via-primary-50/40 to-white shadow-2xl rounded-2xl border-2 border-primary-200/50 overflow-hidden backdrop-blur-sm">
        {/* Header strip */}
        <div className="px-6 py-5 sm:px-8 sm:py-6" style={{ background: 'linear-gradient(to right, #0d1b3a, #1e2848, #eab308)' }}>
            <div className="flex flex-col sm:flex-row sm:items-center sm:justify-between gap-3">
            <div className="flex items-center gap-3">
              <div className="p-2.5 rounded-xl bg-white/25 border border-white/40 shadow-lg backdrop-blur-md text-xl leading-none">
                🔩
              </div>
              <div>
                <h1 className="text-xl sm:text-2xl font-extrabold text-white drop-shadow">
                  Bill of Materials
                </h1>
                <p className="mt-0.5 text-white/90 text-sm">
                  Auto-generated from costing items. Edit brand and specs as needed.
                </p>
              </div>
            </div>

            <div className="flex flex-col gap-2 w-full sm:w-auto flex-shrink-0">
              {/* Status badge row */}
              {source && (
                <div className="flex justify-start sm:justify-end">
                  <span
                    className={`text-xs px-3 py-1.5 rounded-full border font-semibold ${
                      source === 'overrides'
                        ? 'bg-emerald-50 text-emerald-700 border-emerald-200'
                        : source === 'costing-sheet'
                        ? 'bg-yellow-50 text-yellow-700 border-yellow-200'
                        : 'bg-white/20 text-white border-white/40'
                    }`}
                  >
                    {source === 'overrides'
                      ? '✓ Saved overrides'
                      : source === 'costing-sheet'
                      ? '⚡ From Costing Sheet'
                      : 'Auto-generated'}
                  </span>
                </div>
              )}
              {/* Export & Save buttons — hidden for read-only (Ops/Finance/Management) */}
              {canEdit && fields.length > 0 && (
                <div className="flex items-center gap-2 flex-wrap justify-start sm:justify-end">
                  <button
                    type="button"
                    onClick={() => exportBomXlsx(liveRows, storedBom?.sheetName ?? 'BOM')}
                    className="flex items-center gap-1.5 text-xs text-white font-semibold bg-white/20 hover:bg-white/30 border border-white/40 px-3 py-2 rounded-lg transition-colors min-h-[36px]"
                  >
                    📤 XLSX
                  </button>
                  <button
                    type="button"
                    onClick={() => exportBomCsv(liveRows, storedBom?.sheetName ?? 'BOM')}
                    className="flex items-center gap-1.5 text-xs text-white font-semibold bg-white/20 hover:bg-white/30 border border-white/40 px-3 py-2 rounded-lg transition-colors min-h-[36px]"
                  >
                    📤 CSV
                  </button>
                  <button
                    type="button"
                    onClick={() => { void handleSubmit(onSubmit)(); }}
                    disabled={saveStatus === 'saving'}
                    className="flex items-center gap-1.5 bg-white/20 hover:bg-white/30 border-2 border-white/40 text-white text-xs font-semibold px-4 py-2 rounded-xl transition-all min-h-[36px] disabled:opacity-60"
                  >
                    💾 Save BOM
                  </button>
                </div>
              )}
            </div>
          </div>
        </div>

        {/* Content */}
        <div className="px-4 sm:px-6 md:px-8 py-6 sm:py-8">
          {/* Active customer banner */}
          {(() => {
            const ac = getActiveCustomer();
            return ac ? (
              <div className="mb-5 rounded-xl border border-sky-200 bg-sky-50 px-4 py-2.5 flex items-center justify-between gap-3">
                <p className="text-xs text-sky-700">
                  <span className="font-semibold">Active customer:</span> {ac.master.name}
                  {ac.bom && <span className="ml-2 text-emerald-600 font-medium">· BOM saved ✓</span>}
                </p>
                <Link to="/" className="text-xs text-sky-600 hover:text-sky-800 font-medium whitespace-nowrap transition-colors">
                  View Dashboard →
                </Link>
              </div>
            ) : (
              <div className="mb-5 rounded-xl border border-amber-200 bg-amber-50 px-4 py-2.5 flex items-center justify-between gap-3">
                <p className="text-xs text-amber-700">No active customer. BOM will save to scratchpad only.</p>
                <Link to="/customers" className="text-xs text-amber-700 hover:text-amber-900 font-semibold border border-amber-300 hover:bg-amber-100 px-3 py-1 rounded-lg transition-colors whitespace-nowrap">
                  Select Customer →
                </Link>
              </div>
            );
          })()}

          {/* Costing Sheet source banner */}
          {!loading && storedBom && (
            <div className="mb-5 rounded-xl border border-yellow-200 bg-yellow-50 px-4 py-3 flex flex-col sm:flex-row sm:items-center gap-3">
              <div className="flex items-start gap-2 flex-1">
                <span className="text-lg flex-shrink-0">⚡</span>
                <div>
                  <p className="text-sm font-semibold text-yellow-800">
                    BOM auto-generated from Costing Sheet
                  </p>
                  <p className="text-xs text-yellow-700 mt-0.5">
                    Source: <strong>{storedBom.sheetName}</strong> ·{' '}
                    {new Date(storedBom.generatedAt).toLocaleDateString('en-IN', {
                      day: '2-digit', month: 'short', year: 'numeric',
                    })}{' '}
                    {new Date(storedBom.generatedAt).toLocaleTimeString('en-IN', {
                      hour: '2-digit', minute: '2-digit',
                    })}
                    {source === 'overrides' && (
                      <span className="ml-2 text-emerald-700 font-medium">· Your edits are saved</span>
                    )}
                  </p>
                </div>
              </div>
              <div className="flex items-center gap-2 flex-shrink-0">
                {canEdit && source === 'overrides' && (
                  <button
                    type="button"
                    onClick={handleRegenerateFromCosting}
                    className="text-xs text-yellow-700 hover:text-yellow-900 border border-yellow-300 hover:bg-yellow-100 px-3 py-1.5 rounded-lg transition-colors font-medium"
                  >
                    ↺ Reset to generated
                  </button>
                )}
                <span className="text-xs text-yellow-600 bg-yellow-100 border border-yellow-200 px-2.5 py-1 rounded-lg font-medium">
                  {storedBom.rows.length} items
                </span>
              </div>
            </div>
          )}

          {/* Loading */}
          {loading && (
            <div className="flex items-center gap-3 text-secondary-500 text-sm py-12 justify-center">
              <span className="w-4 h-4 border-2 border-primary-200 border-t-primary-600 rounded-full animate-spin" />
              Loading BOM…
            </div>
          )}

          {/* Error */}
          {fetchError && (
            <AlertCard
              variant="warning"
              title="Unable to load BOM"
              message={fetchError}
              className="mb-4"
            />
          )}

          {!loading && !fetchError && (
            <form onSubmit={handleSubmit(onSubmit)}>
              {/* ── Expand / Collapse All bar ── */}
              <div className="flex items-center justify-between mb-3 px-1">
                <span className="text-xs text-secondary-400 font-medium">
                  {fields.length} item{fields.length !== 1 ? 's' : ''} in BOM
                </span>
                <button
                  type="button"
                  onClick={() => setAllCollapsed((v) => !v)}
                  className="flex items-center gap-1.5 text-xs font-semibold px-3 py-1.5 rounded-full border transition-all"
                  style={allCollapsed
                    ? { background: '#0d1b3a', color: 'white', borderColor: '#0d1b3a' }
                    : { background: 'white', color: '#0d1b3a', borderColor: '#0d1b3a40' }
                  }
                >
                  <span>{allCollapsed ? '▶▶' : '▼▼'}</span>
                  {allCollapsed ? 'Expand All' : 'Collapse All'}
                </button>
              </div>

              {/* Grouped BOM table */}
              <BomTable
                fields={fields}
                control={control}
                register={register}
                remove={remove}
                append={append}
                liveRows={liveRows}
                initialRows={initialRowsRef.current}
                allCollapsed={allCollapsed}
                canEdit={canEdit}
              />

              {/* Grand total */}
              {fields.length > 0 && liveRows.some((r) => r.itemName) && (
                <div
                  className="mt-3 rounded-xl overflow-hidden shadow-sm"
                  style={{ background: 'linear-gradient(to right, #0d1b3a, #1e2848, #eab308)' }}
                >
                  <div className="flex items-center justify-between px-4 py-3">
                    <div>
                      <p className="text-sm font-bold text-white uppercase tracking-wide drop-shadow">Total Items in BOM</p>
                      <p className="text-xs text-white/70 mt-0.5">{fields.length} line item{fields.length !== 1 ? 's' : ''}</p>
                    </div>
                    <div className="text-right">
                      <p className="text-xs text-white/70 uppercase tracking-wide">Total Qty</p>
                      <p className="text-lg font-extrabold text-white tabular-nums drop-shadow">
                        {liveRows.reduce((s, r) => s + (parseFloat(r.quantity) || 0), 0).toLocaleString('en-IN')}
                      </p>
                    </div>
                  </div>
                </div>
              )}

              {/* Actions — hidden for read-only; only Expand/Collapse + View Dashboard remain */}
              {canEdit && (
              <div className="mt-4 flex flex-col sm:flex-row sm:items-center sm:justify-between gap-3">
                <div className="flex items-center gap-2 flex-wrap">
                  <button
                    type="button"
                    onClick={() => append({ ...EMPTY_ROW, category: 'others' })}
                    className="flex items-center gap-2 text-sm text-primary-600 hover:text-primary-800 transition-colors px-4 py-2.5 rounded-xl hover:bg-primary-50 border border-primary-200 font-medium min-h-[44px] sm:min-h-0"
                  >
                    <span className="text-lg leading-none">+</span>
                    Add Row (Others)
                  </button>

                </div>

                <div className="flex items-center gap-2 sm:gap-3">
                  {saveStatus === 'saved' && (
                    <span className="text-xs text-emerald-600 font-medium">Saved ✓</span>
                  )}
                  {saveStatus === 'error' && (
                    <span className="text-xs text-red-500">Save failed</span>
                  )}

                  <button
                    type="button"
                    onClick={() => {
                      if (confirm('Clear all BOM rows? This cannot be undone.')) {
                        remove(fields.map((_, i) => i));
                        append({ ...EMPTY_ROW });
                        localStorage.removeItem(getWipKeysForCurrentUser().bomOverrides);
                        setSource('costing-sheet');
                      }
                    }}
                    className="flex-1 sm:flex-none text-sm text-secondary-500 hover:text-secondary-700 transition-colors px-4 py-2.5 rounded-xl hover:bg-secondary-100 border border-secondary-200 min-h-[44px]"
                  >
                    Clear
                  </button>

                  <button
                    type="submit"
                    disabled={saveStatus === 'saving'}
                    className="flex-1 sm:flex-none text-sm disabled:opacity-50 text-white px-6 py-2.5 rounded-xl transition-all font-semibold shadow-lg hover:shadow-xl flex items-center justify-center gap-2 min-h-[44px]"
                    style={{ background: '#0d1b3a' }}
                    onMouseEnter={e => (e.currentTarget.style.background = '#0a1530')}
                    onMouseLeave={e => (e.currentTarget.style.background = '#0d1b3a')}
                  >
                    {saveStatus === 'saving' && (
                      <span className="w-3 h-3 border-2 border-white/30 border-t-white rounded-full animate-spin" />
                    )}
                    Save BOM
                  </button>
                </div>
              </div>
              )}
            </form>
          )}

          {/* GST Summary panel */}
          {!loading && !fetchError && fields.length > 0 && (
            <GstSummaryPanel rows={liveRows} />
          )}

          {/* Info panel */}
          {!loading && !fetchError && (
            <div className="mt-8 bg-gradient-to-br from-primary-50/30 to-transparent border-t border-primary-100 rounded-xl p-5">
              <h3 className="text-sm font-semibold text-secondary-600 mb-3">How BOM generation works</h3>
              <ol className="space-y-2 text-xs text-secondary-500">
                <li className="flex items-start gap-2">
                  <span className="px-2 py-0.5 rounded-md text-xs font-semibold bg-primary-600 text-white shadow-sm flex-shrink-0">1</span>
                  Costing items with the same name and category are grouped and their quantities summed.
                </li>
                <li className="flex items-start gap-2">
                  <span className="px-2 py-0.5 rounded-md text-xs font-semibold bg-primary-600 text-white shadow-sm flex-shrink-0">2</span>
                  Default brand and specification are filled from solar-industry standards per category.
                </li>
                <li className="flex items-start gap-2">
                  <span className="px-2 py-0.5 rounded-md text-xs font-semibold bg-primary-600 text-white shadow-sm flex-shrink-0">3</span>
                  Edit any field and click <strong className="text-secondary-700">Save BOM</strong> — your overrides are persisted and won't be overwritten on the next load.
                </li>
                <li className="flex items-start gap-2">
                  <span className="px-2 py-0.5 rounded-md text-xs font-semibold bg-primary-600 text-white shadow-sm flex-shrink-0">4</span>
                  Add manual rows for items not in the costing sheet (accessories, transport, etc.).
                </li>
              </ol>
            </div>
          )}
        </div>
      </div>
    </div>
  );
}
