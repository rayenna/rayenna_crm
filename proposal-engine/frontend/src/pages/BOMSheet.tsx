interface BomRow {
  category: string;
  itemName: string;
  specification: string;
  quantity: string;
  brand: string;
  isAutoGenerated: boolean;
}

interface FormValues {
  rows: BomRow[];
}


const EMPTY_ROW: BomRow = {
  category: 'others',
  itemName: '',
  specification: '',
  quantity: '1',
  brand: '',
  isAutoGenerated: false,
};

/** Ensure legacy rows have a category and migrate old keys to new ones */
function normalizeRow(r: Partial<BomRow>): BomRow {
  return {
    category:        snapCategory(r.category ?? 'others', r.itemName ?? ''),
    itemName:        r.itemName ?? '',
    specification:   r.specification ?? '',
    quantity:        r.quantity ?? '1',
    brand:           r.brand ?? '',
    isAutoGenerated: r.isAutoGenerated ?? false,
  };
}

interface BomOverrides {
  sheetId: string;
  rows:    BomRow[];
  savedAt: string;
}

function loadOverrides(): BomOverrides | null {
  try {
    const key = getWipKeysForCurrentUser().bomOverrides;
    const raw = localStorage.getItem(key);
    return raw ? JSON.parse(raw) : null;
  } catch { return null; }
}

function persistOverrides(sheetId: string, rows: BomRow[]) {
  const wip = getWipKeysForCurrentUser();
  const data: BomOverrides = { sheetId, rows, savedAt: new Date().toISOString() };
  localStorage.setItem(wip.bomOverrides, JSON.stringify(data));
}

function loadStoredBom(): StoredBom | null {
  try {
    const key = getWipKeysForCurrentUser().bomCosting;
    const raw = localStorage.getItem(key);
    return raw ? JSON.parse(raw) : null;
  } catch { return null; }
}

