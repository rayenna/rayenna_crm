/**
 * Derive applicable GST rate from item name keywords.
 * PV Modules & on-grid Inverters → 5%  (post 56th GST Council, Sep 2025)
 * All other items (structure, boards, cables, earthing, meter, installation, others) → 18%
 */
function deriveGstRate(itemName: string): number {
  const n = itemName.toLowerCase();
  if (
    n.includes('module') || n.includes('panel') || n.includes('solar cell') ||
    n.includes('perc') || n.includes('bifacial') || n.includes('mono') || n.includes('pv ') ||
    n.includes('inverter') || n.includes('string inverter') || n.includes('on-grid') ||
    n.includes('ongrid') || n.includes('grid tie') || n.includes('grid-tie')
  ) return 5;
  return 18;
}

